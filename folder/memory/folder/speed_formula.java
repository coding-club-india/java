package memory.folder;

public class speed_formula
 {

   
   static int speed(int a, int b)
   {
     int ans = a/b ;
     return ans ;
   }
   public static void main(String[] args)
     {
        int d =50 ;
        int t = 5 ;
        int s = speed(d,t);  
        System.out.println("speed formula="+ s);
        
    }
}
